//     Задача2. Написать функцию, которая принимает аргументом число(больше 0)
//     и возвращает массив чисел от 0 до этого числа
function newArray(x, y) {
    let array = []
    for (let i = x; i <= y; i++) array.push(i)
    return array
  }

